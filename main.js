const Discord = require("discord.js");//discord API
const { prefix, token } = require("./config.json"); // config file
const ytdl = require("ytdl-core"); // the youtuber downloader depend
const client = new Discord.Client(); // the bot itself
const fs = require('fs'); // node fileservice

//this handles the commands folder and parses them here
client.commands = new Discord.Collection(); // command parser

const commandFiles = fs.readdirSync('./commands/').filter(file => file.endsWith('.js'));
for(const file of commandFiles){
	const command = require(`./commands/${file}`);

	client.commands.set(command.name, command);
}
